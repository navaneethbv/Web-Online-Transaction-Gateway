public class Grid {

	public String getGridString(int number) {
		
		String str = "";
		
		if (number == 1)
			str="A";
		if (number == 2)
			str="B";
		if (number == 3)
			str="C";
		if (number == 4)
			str="D";
		if (number == 5)
			str="E";
		if (number == 6)
			str="F";
		if (number == 7)
			str="G";
		if (number == 8)
			str="H";
		if (number == 9)
			str="I";
		if (number == 10)
			str="J";
		if (number == 11)
			str="K";
		if (number == 12)
			str="L";
		if (number == 13)
			str="M";
		if (number == 14)
			str="N";
		if (number == 15)
			str="O";
		if (number == 16)
			str="P";
	
		return str;
	}
	
}
